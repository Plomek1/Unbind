using UnityEngine;

namespace Unbind
{
    public class ReflectionTraitLogic : TraitLogic
    {
        public override void Init(bool activeAtAwake)
        {
            base.Init(activeAtAwake);
        }

        protected override void Activate()
        {
            base.Activate();
        }

        protected override void Deactivate()
        {
            base.Deactivate();
        }
    }
}
